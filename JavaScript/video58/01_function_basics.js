// ✅ Function Basics
// Javascript me function ka matlab hota hai ek reusable block of code 
// jo kisi specific task ko perform karta hai. Yeh ek baar likhne ke baad 
// baar baar call kiya ja sakta hai – reuseable aur modular code ke liye best hai!

function greet(name) {
    console.log("Hello, " + name + "!");
}

// Hum function ko call karte hain uske naam se, aur jo arguments chahiye hote hain wo dete hain.
greet("Muzammil"); // Output: Hello, Muzammil!
