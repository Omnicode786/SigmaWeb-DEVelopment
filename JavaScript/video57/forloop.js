var a = 1;

for (var i = 0; i <= 100; i++) {
    console.log("Iteration " + i + ": a = " + a);
    a += 2; // Increment a by 2 in each iteration
}

// explain in roman urdu 
console.log("Loop completed!");
// for loop is used to repeat a block of code a certain number of times.
// In this case, it runs from 0 to 100, incrementing 'a' by 2 each time.
